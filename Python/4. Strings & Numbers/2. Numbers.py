# Working with Numbers

print(2)          # Numbers get printed normal. Integer
print(2.2323)     # Numbers get printed normal. Float
print(-2.2323)    # Numbers get printed normal. Negative numbers

# Arithmetic
print(3+3)

print(3*5+3)      # Order of operation
print(3*(5+3))
print(10 % 3)

num = 4           # storing values and printing

print(num)

# Converting to string

print(str(num))
print("I'm " + str(num) + " year old")       # converting int to string

# Using Functions with the numbers

num = -4
print(abs(num))                     # absolute Value

num = 4
print(pow(num, 2))                  # Powers - 4^2
print(max(4, 2345, 5, 543, 546,))   # Displays MAx
print(min(4, 2345, 5, 543, 546,))   # Displays Min
print(round(3.323))                 # rounding up numbers.
